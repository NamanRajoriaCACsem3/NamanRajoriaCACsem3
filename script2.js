const globalizationVideo = document.getElementById("globalization-video-source");
const videoContainer = document.getElementById("globalization-video");

videoContainer.addEventListener("mouseover", function () {
    // Play the video when the cursor is over the video container
    globalizationVideo.play();
});

videoContainer.addEventListener("mouseout", function () {
    // Pause the video when the cursor leaves the video container
    globalizationVideo.pause();
});

